def translate_phrase(text: str) -> str:
    tokens = text.replace('-', ' ').split()
    translated = [translation_map.get(token.lower(), token) for token in tokens]
    return ' '.join(translated)


def make_example(phrase: str, category: str) -> str:
    templates = example_templates.get(category, ["This text includes {phrase} in a sentence."])
    template = templates[hash(phrase) % len(templates)]
    return template.format(phrase=phrase)

